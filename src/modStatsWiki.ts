import { TriggerContext } from '@devvit/public-api';
import { formatUsername } from './utility.js';

const WIKI_PAGE = 'mod-stats';
const REDIS_PREFIX = 'modstats:';

export async function updateWikiPage(context: TriggerContext): Promise<void> {
  const subreddit = await context.reddit.getCurrentSubreddit();
  const redis = context.redis;

  // Gather stats from Redis
  const allKeys = await redis.zRange(`${REDIS_PREFIX}leaderboard`, 0, 19, { by: 'rank', reverse: true });

  if (!allKeys || allKeys.length === 0) {
    console.log('[mod-stats] No leaderboard data yet, skipping wiki update.');
    return;
  }

  const lines: string[] = [
    `# Mod Stats for r/${subreddit.name}`,
    '',
    `*Last updated: ${new Date().toISOString()}*`,
    '',
    '## Top Moderators (by action count)',
    '',
    '| Rank | Moderator | Actions |',
    '|------|-----------|---------|',
  ];

  allKeys.forEach((entry, idx) => {
    lines.push(`| ${idx + 1} | u/${formatUsername(entry.member)} | ${entry.score} |`);
  });

  lines.push('');
  lines.push('---');
  lines.push('*Generated by mod-stats app*');

  const content = lines.join('\n');

  try {
    await subreddit.wiki.getPage(WIKI_PAGE);
    await subreddit.wiki.updatePage({
      page: WIKI_PAGE,
      content,
      reason: 'Auto-update mod stats',
    });
  } catch {
    await subreddit.wiki.createPage({
      page: WIKI_PAGE,
      content,
      reason: 'Initial mod stats page',
    });
  }

  console.log('[mod-stats] Wiki page updated successfully.');
}
